const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let text = "";
    for (let i = 0; i < expr.length; i += 10) {

        if (expr[i] !== '*') {
            let letter = "";
            for (let j = i; j < 10 + i; j += 2) {

                if ((expr[j] + expr[j + 1]) === '10') {
                    letter += '.'
                }
                if ((expr[j] + expr[j + 1]) === '11') {
                    letter += '-'
                }
            }
            text += MORSE_TABLE[letter];
        } else {
            text += ' '
        }

    }

    return text;
}

module.exports = {
    decode
}